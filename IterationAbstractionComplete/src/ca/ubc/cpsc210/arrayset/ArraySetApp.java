package ca.ubc.cpsc210.arrayset;

public class ArraySetApp {

	/*
	 * Sample app to demonstrate iteration over ArraySet
	 */
	public static void main(String args[]) {
		ArraySet<Integer> mySet = new ArraySet<Integer>();
		
		
		mySet.add(1);
		mySet.add(2);
		mySet.add(3);
		
		for (Integer i: mySet) 
			System.out.println(i);
	}
}
